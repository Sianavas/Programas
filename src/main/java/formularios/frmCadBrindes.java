    private void jtEventosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtEventosMouseClicked
       if (jtEventos.getSelectedRow() != -1){
        
        txtBrinde.setText(jtEventos.getValueAt(jtEventos.getSelectedRow(),0).toString());
        txtCor.setText(jtEventos.getValueAt(jtEventos.getSelectedRow(),1).toString());
        txtQtde.setText(jtEventos.getValueAt(jtEventos.getSelectedRow(),2).toString());
        txtFornecedor.setText(jtEventos.getValueAt(jtEventos.getSelectedRow(),3).toString()); }
    }//GEN-LAST:event_jtEventosMouseClicked

