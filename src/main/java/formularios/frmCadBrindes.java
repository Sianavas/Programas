    private void BtnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCadastrarActionPerformed
        String Teste;  
        int Validador = 0;
        DefaultTableModel dtmEventos = (DefaultTableModel) jtEventos.getModel();
        Object[] dados = {txtBrinde.getText(),txtCor.getText(),
                    txtQtde.getText(), txtFornecedor.getText()};
        for (int i = 0; i < 4; i++) {
            Teste = (String) dados[i];
            if (Teste.equals("")){
                JOptionPane.showMessageDialog(null,"Preencha Todos os campos!");
                Validador = 1;
                break;
            }
        }
        if (Validador == 0){
            dtmEventos.addRow(dados);
            txtBrinde.setText("");
            txtCor.setText("");
            txtQtde.setText("");
            txtFornecedor.setText("");
        }
        
    }//GEN-LAST:event_BtnCadastrarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (jtEventos.getSelectedRow() != -1){ 
        DefaultTableModel dtmEventos = (DefaultTableModel) jtEventos.getModel();
        dtmEventos.removeRow(jtEventos.getSelectedRow());
        txtBrinde.setText("");
        txtCor.setText("");
        txtQtde.setText("");
        txtFornecedor.setText("");
        }else{
            JOptionPane.showMessageDialog(null,"Selecione um Brinde para excluir!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

