    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if (jtEventos.getSelectedRow() != -1){
            jtEventos.setValueAt(txtBrinde.getText(),jtEventos.getSelectedRow(),0);
            jtEventos.setValueAt(txtCor.getText(),jtEventos.getSelectedRow(),1);
            jtEventos.setValueAt(txtQtde.getText(),jtEventos.getSelectedRow(),2);
            jtEventos.setValueAt(txtFornecedor.getText(),jtEventos.getSelectedRow(),3);
        }
        txtBrinde.setText("");
        txtCor.setText("");
        txtQtde.setText("");
        txtFornecedor.setText("");
        int Selecionado = jtEventos.getSelectedRow();
        jtEventos.getSelectionModel().removeSelectionInterval(Selecionado, Selecionado);
    }//GEN-LAST:event_btnAtualizarActionPerformed

