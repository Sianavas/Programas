    private void BtnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCadastrarActionPerformed
        String Teste;  
        int Validador = 0;
        DefaultTableModel dtmEventos = (DefaultTableModel) jtEventos.getModel();
        Object[] dados = {txtLocal.getText(),txtHorario.getText(),
                    txtEvento.getText(), txtResponsavel.getText()};
        for (int i = 0; i < 4; i++) {
            Teste = (String) dados[i];
            if (Teste.equals("")){
                JOptionPane.showMessageDialog(null,"Preencha Todos os campos!");
                Validador = 1;
                break;
            }
        }
        if (Validador == 0){
            dtmEventos.addRow(dados);
            txtLocal.setText("");
            txtHorario.setText("");
            txtEvento.setText("");
            txtResponsavel.setText("");
        }
        
    }//GEN-LAST:event_BtnCadastrarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (jtEventos.getSelectedRow() != -1){ 
        DefaultTableModel dtmEventos = (DefaultTableModel) jtEventos.getModel();
        dtmEventos.removeRow(jtEventos.getSelectedRow());
        txtLocal.setText("");
        txtHorario.setText("");
        txtEvento.setText("");
        txtResponsavel.setText("");
        }else{
            JOptionPane.showMessageDialog(null,"Selecione um Evento para excluir!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

