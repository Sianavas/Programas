    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if (jtEventos.getSelectedRow() != -1){
            jtEventos.setValueAt(txtLocal.getText(),jtEventos.getSelectedRow(),0);
            jtEventos.setValueAt(txtHorario.getText(),jtEventos.getSelectedRow(),1);
            jtEventos.setValueAt(txtEvento.getText(),jtEventos.getSelectedRow(),2);
            jtEventos.setValueAt(txtResponsavel.getText(),jtEventos.getSelectedRow(),3);
        }
        txtLocal.setText("");
        txtHorario.setText("");
        txtEvento.setText("");
        txtResponsavel.setText("");
        int Selecionado = jtEventos.getSelectedRow();
        jtEventos.getSelectionModel().removeSelectionInterval(Selecionado, Selecionado);
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void jtEventosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtEventosMouseClicked
      if (jtEventos.getSelectedRow() != -1){
        
        txtLocal.setText(jtEventos.getValueAt(jtEventos.getSelectedRow(),0).toString());
        txtHorario.setText(jtEventos.getValueAt(jtEventos.getSelectedRow(),1).toString());
        txtEvento.setText(jtEventos.getValueAt(jtEventos.getSelectedRow(),2).toString());
        txtResponsavel.setText(jtEventos.getValueAt(jtEventos.getSelectedRow(),3).toString());
    }
    }//GEN-LAST:event_jtEventosMouseClicked

